int	ft_sqrt(int nb)
{
	int	tmp;

	tmp = 2;
	if (nb == 1)
		return (1);
	while (tmp * tmp <= nb && tmp < 46341)
	{
		if (tmp * tmp == nb)
			return (tmp);
		tmp++;
	}
	return (0);
}
